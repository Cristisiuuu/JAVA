class ImprimeNomeWhile{
    public static void main(String[] args){
        //quando utilizamos o while para repetir, temos que definir o contador fora dele.
        int i =1;
        // utilizaremos um while para fazer a repetição, mas também da para utiliza o for.
        while(i <= 100){
            System.out.println(i+" Cristiano Marques Silva");
            i++;
        }
    }
}